// rules for naming variables

// you cannot start with number

// example :-

// 1value (invalid)
// value1 (valid)

var value1 = 2;
console.log(value1);
console.log(value1 ** 3);
console.log(value1 ** 0.5);
console.log(value1 - 2);
console.log(value1 + 2);
console.log(value1 / 2);
console.log(value1 % 2);

// you can use only undersore _ or dollar symbol

// first_name (valid)

var first_name = "kanchan";

// _firstname (valid)

var _firstname = "sannch";

// first$name (valid)
var first$name = "Sunny";
// $firstname (valid)
var $firstname = "Aishu";

console.log(first_name);
console.log(_firstname);
console.log(first$name);
console.log($firstname);

